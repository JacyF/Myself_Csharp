// 5. Write a C# Sharp program to swap two numbers. 
// Test Data:
// Input the First Number : 5
// Input the Second Number : 6
// Expected Output: 
// After Swapping :
// First Number : 6 
// Second Number : 5 

Console.WriteLine("Please , introduce to numbers to swap ");
int first = Convert.ToInt32(Console.ReadLine());
int second = Convert.ToInt32(Console.ReadLine());

Console.WriteLine($"First number : {first} ");
Console.WriteLine($"Second number : {second} ");

Console.WriteLine("After swapping ");
Console.WriteLine($"First number : {second} ");
Console.WriteLine($"Second number : {first} ");
